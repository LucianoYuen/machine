void led_flash()
{
	for(uint8_t x = 0 ; x < 4 ; x++)
	{
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_1|GPIO_PIN_2, GPIO_PIN_SET);
		HAL_Delay(100);
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_1|GPIO_PIN_2, GPIO_PIN_RESET);
		HAL_Delay(100);
	}
}

